package comercio.movil;

public class Producto {
	  private String idProd; 
	  private String nombreProd; 
	  private String descripProd; 
	  private String modeloProd; 
	  private String cantidadProd; 
	  private String imagenProd; 
	  private String precioProd; 
	  private String fabricanteProd;
	public String getIdProd() {
		return idProd;
	}
	public void setIdProd(String idProd) {
		this.idProd = idProd;
	}
	public String getNombreProd() {
		return nombreProd;
	}
	public void setNombreProd(String nombreProd) {
		this.nombreProd = nombreProd;
	}
	public String getDescripProd() {
		return descripProd;
	}
	public void setDescripProd(String descripProd) {
		this.descripProd = descripProd;
	}
	public String getModeloProd() {
		return modeloProd;
	}
	public void setModeloProd(String modeloProd) {
		this.modeloProd = modeloProd;
	}
	public String getCantidadProd() {
		return cantidadProd;
	}
	public void setCantidadProd(String cantidadProd) {
		this.cantidadProd = cantidadProd;
	}
	public String getImagenProd() {
		return imagenProd;
	}
	public void setImagenProd(String imagenProd) {
		this.imagenProd = imagenProd;
	}
	public String getPrecioProd() {
		return precioProd;
	}
	public void setPrecioProd(String precioProd) {
		this.precioProd = precioProd;
	}
	public String getFabricanteProd() {
		return fabricanteProd;
	}
	public void setFabricanteProd(String fabricanteProd) {
		this.fabricanteProd = fabricanteProd;
	}
	  
	  
}
